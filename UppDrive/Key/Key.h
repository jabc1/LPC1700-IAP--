#ifndef __KEY_H 
#define __KEY_H

/********************************************************************************************************
* 【管脚定义】 pin definition                                                
********************************************************************************************************/
#define KEY_UP_FUN            (PIO0_10_GPIO)                                     // P0.10    
#define KEY_UP_PORT       	  (PORT0)							                 // 端口
#define KEY_UP_PIN       	  (10)							                 	 // 管脚

#define KEY_DN_FUN            (PIO2_13_GPIO)                                     // P2.13    
#define KEY_DN_PORT       	  (PORT2)							                 // 端口
#define KEY_DN_PIN       	  (13)								                 // 管脚	

#define KEY_MD_FUN            (PIO2_12_GPIO)                                     // P2.12    
#define KEY_MD_PORT       	  (PORT2)						                 	 // 端口
#define KEY_MD_PIN       	  (12)								                 // 管脚

#define KEY_LF_FUN            (PIO2_11_GPIO)                                     // P2.11    
#define KEY_LF_PORT       	  (PORT2)							                 // 端口
#define KEY_LF_PIN       	  (11)								                 // 管脚	

#define KEY_RT_FUN            (PIO2_10_GPIO)                                     // P2.10    
#define KEY_RT_PORT       	  (PORT2)						                 	 // 端口
#define KEY_RT_PIN       	  (10)								                 // 管脚

/********************************************************************************************************
* 【管脚方向】 Pin direction                                                
********************************************************************************************************/
#define KEY_SET_DIR(port, pin)     GPIOSetDir(port, pin, GPIO_IN)				 // 输入

/********************************************************************************************************
* 【管脚读】 Pin Read                                                
********************************************************************************************************/
#define KEY_READ_STATUS(port, pin) GPIOStatusValue(port, pin)				     // 获取按键

/********************************************************************************************************
* 【宏定义】 macro definition                                                 
********************************************************************************************************/
typedef enum
{
  	KEY_VAL_NO = 0x00,					                                         // 无按键
    KEY_VAL_UP = 0x01,					                                         // 上键
    KEY_VAL_DN = 0x02,					                                         // 下键
    KEY_VAL_MD = 0x03,					                                         // 中键
    KEY_VAL_LF = 0x04,					                                         // 左键
    KEY_VAL_RT = 0x05,					                                         // 右键

} KEY_VAL;

/********************************************************************************************************
* 【内部函数】 internal function                                              
********************************************************************************************************/
static void Key_DelayMs(u8 t);      											 // ms延时
static u8   Key_GetStatus(u8 portNum, u8 bitPosi);								 // 获取按键状态

/********************************************************************************************************
* 【全局函数】 global function                                                 
********************************************************************************************************/
extern void KeyInit(void);			                                             // 初始化按键
extern u8   KeyGetValue(void);	     	                                         // 获取按键

/*******************************************************************************************************/
#endif

