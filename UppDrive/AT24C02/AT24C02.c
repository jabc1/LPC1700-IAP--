/********************************************************************************************************
* FunctionName   : AT_DelayMs()
* Description    : 延时
* EntryParameter : ms - 时间参数
* ReturnValue    : None
********************************************************************************************************/
void AT_DelayMs(u16 ms)
{
    u16 i;

	while (ms--)
	{
	    for (i=16663; i>0; i--)	
		{
		    ;
		}
	}
}
				
/********************************************************************************************************
* FunctionName   : ATReadNByte()
* Description    : 读多字节数据
* EntryParameter : startAddr-起始地址；pDat-数据缓冲；num-个数
* ReturnValue    : None
********************************************************************************************************/
void ATReadNByte(u8 startAddr, u8 *pDat, u8 num)
{
    u8 i;
	
	for (i=0; i<num; i++)    
	{
	    *(pDat+i) = AT_ReadByte(startAddr+i);                                 // 读数据
	}
}

/********************************************************************************************************
* FunctionName   : ATWriteNByte()
* Description    : 写多字节数据
* EntryParameter : startAddr-起始地址；pDat-数据缓冲；num-个数
* ReturnValue    : None
********************************************************************************************************/
void ATWriteNByte(u8 startAddr, u8 *pDat, u8 num)
{
    u8 i;
	
	for (i=0; i<num; i++)    
	{
	    AT_WriteByte(startAddr+i, *(pDat+i));                                // 写数据
		AT_DelayMs(10);						                                 // 适当延时，非常重要
	}    
}

