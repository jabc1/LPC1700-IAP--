#ifndef __IRCONTROL_H 
#define __IRCONTROL_H

/********************************************************************************************************
* 【管脚定义】 pin definition                                                
********************************************************************************************************/
#define IRC_IO_FUN            (PIO0_11_GPIO)                                     // P0.11    
#define IRC_IO_PORT       	  (PORT0)							                 // 端口
#define IRC_IO_PIN       	  (11)							                 	 // 管脚

/********************************************************************************************************
* 【管脚方向】 Pin direction                                                
********************************************************************************************************/
#define IRC_IO_OUT()          GPIOSetDir(IRC_IO_PORT, IRC_IO_PIN, GPIO_IN)	     // 设置端口方向

/********************************************************************************************************
* 【管脚写】 Pin Write                                                
********************************************************************************************************/
#define	IRC_IO_READ()         (GPIOStatusValue(IRC_IO_PORT, IRC_IO_PIN))         // 读管脚

/********************************************************************************************************
* 【按键定义】 Key definitions                                                
********************************************************************************************************/
#define IRC_OK                (0x00)								             // 确定
#define IRC_LT                (0x01)								             // 左
#define IRC_RT                (0x02)								             // 右
#define IRC_UP                (0x03)								             // 上
#define IRC_DN                (0x04)								             // 下
#define IRC_NL                (0xFF)								             // 没有

/********************************************************************************************************
* 【内部函数】 internal function                                              
********************************************************************************************************/
static void IRC_DelayUs(u16 us);        			                             // 延时

/********************************************************************************************************
* 【全局函数】 global function                                                 
********************************************************************************************************/
extern void IRCInit(void);                                                       // 初始化红外端口
extern u8   IRCDecode(void);                                                     // 红外解码函数
extern u8   IRCGetValue(void);							                         // 获取红外按键

/******************************************************************************************************/
#endif

