#ifndef __BUZZER_H 
#define __BUZZER_H

/********************************************************************************************************
* Pin Definition                                                 
********************************************************************************************************/ 
#define BZR_CTRL_FUN          (PIO0_4_GPIO)                                      // P0.4 
#define BZR_CTRL_PORT         (PORT0)							                 // 端口
#define BZR_CTRL_PIN       	  (4)								                 // 管脚

/********************************************************************************************************
* 【管脚方向】 Pin direction                                                
********************************************************************************************************/
#define BZR_CTRL_OUT()        GPIOSetDir(BZR_CTRL_PORT, BZR_CTRL_PIN, GPIO_OUT)	 // 设置端口方向

/********************************************************************************************************
* 【管脚写】 Pin Write                                                
********************************************************************************************************/
#define BZR_CTRL_WRITE(sw)    GPIOSetValue(BZR_CTRL_PORT, BZR_CTRL_PIN, sw)      // 端口输出

/********************************************************************************************************
* Macro Definition                                                
********************************************************************************************************/
typedef enum
{
    BZR_ON  = 0x00,									                             // 蜂鸣器打开
    BZR_OFF = 0x01,									                             // 蜂鸣器关闭

} BZR_CTRL_STA;

/********************************************************************************************************
* Internal Function                                                  
********************************************************************************************************/
static void BUZ_DelayMs(u16 ms);							                     // 延时

/********************************************************************************************************
* Global Function                                                 
********************************************************************************************************/
extern void BUZInit(void);									                     // 初始化
extern void BUZCtrl(u8 sw);														 // 开关控制
extern void BUZSound(u8 times, u16 onTime, u16 offTime);		                 // 蜂鸣器响声

/*******************************************************************************************************/
#endif

